/**
 * Search cities by partial text match
 * @param {string} query - User input
 * @returns {Array} Matching cities
 */
export function searchCities(query) {
  if (!query || query.length < 2) return [];

  const searchTerm = query.toLowerCase().trim();
  const isZipSearch = /^\d/.test(searchTerm);

  if (isZipSearch) {
    // Search by zip code
    return US_CITIES
      .filter(city => city.zip.startsWith(searchTerm))
      .slice(0, 10)
      .map(city => ({
        ...city,
        display: `${city.city}, ${city.state} ${city.zip}`,
        label: `${city.city}, ${city.state}`,
      }));
  } else {
    // Search by city name
    return US_CITIES
      .filter(city => city.city.toLowerCase().startsWith(searchTerm))
      .slice(0, 10)
      .map(city => ({
        ...city,
        display: `${city.city}, ${city.state}`,
        label: `${city.city}, ${city.state}`,
      }));
  }
}

export default US_CITIES;
